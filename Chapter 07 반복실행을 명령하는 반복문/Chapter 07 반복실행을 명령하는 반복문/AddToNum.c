#include <stdio.h>

int main(void) {
	int total = 0;
	int i, num;
	printf("0부터 num까지의 덧셈, num은? : ");
	scanf_s("%d", &num);

	for (i = 0; i < num + 1; i++) // i <= num도 가능
//  for (int i = 0; i < num + 1; i++)도 가능하나 지원하지 않는 컴파일러도 존재하기 때문에 for문 밖에서 변수를 선언해야 한다.
		total += i;

	printf("0부터 %d까지 덧셈결과 : %d \n", num, total);
	return 0;
}

